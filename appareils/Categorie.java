package appareils;

/**
 * Enumeration representant les differentes categories d'appareils disponibles.
 */
public enum Categorie {

    /** Categorie pour les telephones. */
    TELEPHONE,

    /** Categorie pour les ordinateurs portables. */
    ORDINATEURPORTABLE,

    /** Categorie pour les ordinateurs de marque Apple. */
    APPLE,

    /** Categorie pour les ordinateurs de marque Asus. */
    ASUS
}
